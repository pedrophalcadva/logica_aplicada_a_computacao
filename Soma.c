#include <stdio.h>

int main()
{//Início

    int n1, n2, soma;
    
    printf("Digite o primeiro valor: \n");
    
    scanf("%d",&n1);
    
    printf("Digite o segundo valor: \n");
    
    scanf("%d",&n2);
    
    soma = n1 + n2;
    
    printf("A soma de %d + %d é %d", n1, n2, soma);
    
    return 0;
    
}//Fim

